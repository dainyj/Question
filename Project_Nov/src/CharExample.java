public class CharExample {
	public static void main(String[] args) {
		char c1 = 'A';
		char c2 = 65;
		int a3 = 'A';
		int a4 = 'a';		
		//char은 문자, int는 정수, A=65(ASCII코드 참고) a=97
		
		System.out.println(c1);
		System.out.println(c2);
		System.out.println(a3);
		System.out.println(a4);		
	}
}
