public class PPExample {
	public static void main(String[] args) {
		int A = 5;
		
		System.out.println(A++);
		System.out.println(A);
		System.out.println();
	
		System.out.println(++A);
		System.out.println(A);
		System.out.println(A--);
		System.out.println();
		System.out.println(--A);
	}
}
